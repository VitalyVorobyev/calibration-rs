//! `generate-manifest` — heuristic dataset folder → `dataset.toml` skeleton.
//!
//! Walks a dataset directory and prints a best-effort `DatasetSpec` as TOML
//! to stdout. Fields the heuristics cannot determine (board geometry, target
//! kind, frame convention, ambiguous topology) are emitted at placeholder
//! values and listed under `_unresolved`; the runner refuses the manifest
//! until that list is cleared (ADR 0019). This is the CLI front end to
//! [`sniff_folder`], the same inference the app's "Sniff folder" command uses.
//!
//! Usage: `generate-manifest <dataset-folder>`
//!
//! Requires the `cli` feature:
//! `cargo run -p vision-calibration-dataset --features cli --bin generate-manifest -- <folder>`

use std::path::Path;
use std::process::ExitCode;

use vision_calibration_dataset::sniff_folder;

fn main() -> ExitCode {
    let mut args = std::env::args_os().skip(1);
    let Some(folder) = args.next() else {
        eprintln!("usage: generate-manifest <dataset-folder>");
        return ExitCode::from(2);
    };
    if args.next().is_some() {
        eprintln!("error: expected exactly one argument (the dataset folder)");
        return ExitCode::from(2);
    }

    let root = Path::new(&folder);
    let spec = match sniff_folder(root) {
        Ok(spec) => spec,
        Err(err) => {
            eprintln!("error: {err}");
            return ExitCode::FAILURE;
        }
    };

    match toml::to_string_pretty(&spec) {
        Ok(toml) => {
            print!(
                "# Auto-generated by `generate-manifest`. Review every field and\n\
                 # clear `_unresolved` before running — the runner rejects a\n\
                 # manifest with a non-empty `_unresolved` list (ADR 0019).\n\n{toml}"
            );
            ExitCode::SUCCESS
        }
        Err(err) => {
            eprintln!("error serializing manifest: {err}");
            ExitCode::FAILURE
        }
    }
}
